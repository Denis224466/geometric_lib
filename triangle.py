def area(a, h):
    '''Принимает 2 числа a и h(основание и высота треугольника), возвращает площадь треугольника с высотой h и основанием a'''
    return a * h / 2

def perimeter(a, b, c):
    '''Принимает 3 числа a,b,c(стороны трекгольника), аозвращает их сумму(периметр этого треугольника)'''
    return a + b + c
