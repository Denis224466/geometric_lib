def area(a, b):
    '''Принимает 2 числа a и b(смежные стороны прямоугольника), возвращает произведение чисел a и b(площадь этого прямоугольника)'''
    return a * b

def perimeter(a, b):
    '''Принимает 2 числа a и b(стороны прямоугольника), возвращает удвоенную сумму чисел a и b(периметр этого прямоугольника)'''
    return (a + b)*2

